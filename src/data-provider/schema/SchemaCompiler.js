    '  if not exists (select from pg_roles where rolname = \'lexai_admin\') then',
    '    create role lexai_admin;',
    '  end if;',
    '  if not exists (select from pg_roles where rolname = \'lexai_manager\') then',
    '    create role lexai_manager;',
    '  end if;',

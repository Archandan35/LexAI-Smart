    '  insert into migration_registry (id, version, description, sql_hash, applied_at, duration_ms, success)',
    '  values (gen_random_uuid()::text, 0, \'safe_ddl\', md5(sql), now(), 0, true);',
    '  execute sql;',
    'end;',
